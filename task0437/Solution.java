package com.javarush.task.task04.task0437;


/* 
Треугольник из восьмерок
*/

public class Solution {
    public static void main(String[] args) throws Exception {

        int z=0;
        int m=10;
        String s= "";

        for ( z=0;z < m; z++)
        {
            s = s + "8";
            System.out.println(s);
        }
    }
}
